///----------------------------------------------------------------------------
/// Headers
///----------------------------------------------------------------------------
#include "BeamGen.hh"



///----------------------------------------------------------------------------
/// Constructor
///----------------------------------------------------------------------------
BeamGen::BeamGen(): m_E(200.), m_dE(0.), m_ex(0.), m_ey(0.), m_bx(0.), m_ax(0.), m_by(0.), m_ay(0.), m_xmax(10.), m_ymax(.25)
{
}



///----------------------------------------------------------------------------
/// Destructor
///----------------------------------------------------------------------------
BeamGen::~BeamGen()
{
}



///----------------------------------------------------------------------------
/// Set beam kinetic energy
///----------------------------------------------------------------------------
void BeamGen::SetEnergy(double E)
{
	m_E = E;

	return;
}



///----------------------------------------------------------------------------
/// Set beam kinetic energy dispersion
///----------------------------------------------------------------------------
void BeamGen::SetEnergyDispersion(double dE)
{
	m_dE = dE;

	return;
}



///----------------------------------------------------------------------------
/// Set beam emittance
///----------------------------------------------------------------------------
void BeamGen::SetEmittance(double ex, double ey)
{
	m_ex = ex;
	m_ey = ey;

	return;
}



///----------------------------------------------------------------------------
/// Set twiss parameters
///----------------------------------------------------------------------------
void BeamGen::SetTwiss(double bx, double ax, double by, double ay)
{
	m_bx = bx;
	m_ax = ax;
	m_by = by;
	m_ay = ay;

	return;
}



///----------------------------------------------------------------------------
/// Set area limit of beam destination in z plane
///----------------------------------------------------------------------------
void BeamGen::SetMax(double xmax, double ymax)
{
	m_xmax = xmax;
	m_ymax = ymax;

	return;
}



///----------------------------------------------------------------------------
/// Generate beam particle from given properties
///----------------------------------------------------------------------------
void BeamGen::Generate(double& pxi, double& pyi, double& pzi, double& ei, double& xi, double& yi)
{
	//----------------------------------------------------------
	// Ideal case
	//----------------------------------------------------------
	pxi = 0.;
	pyi = 0.;
	pzi = 1.;
	ei  = 200.;
	xi  = 0.;
	yi  = 0.;


	return;
}


